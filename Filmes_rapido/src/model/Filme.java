package model;

import java.util.ArrayList;
import java.util.List;

/**
 *
 * @author alan
 */
public class Filme implements java.io.Serializable{
    private String titulo;
    private int ano;
    private ArrayList<Integer> copias;
    private String genero;

    public Filme() {
    }

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    public int getAno() {
        return ano;
    }

    public void setAno(int ano) {
        this.ano = ano;
    }

    public ArrayList<Integer> getCopias() {
        return copias;
    }

    public void setCopias(ArrayList<Integer> copias) {
        this.copias = copias;
    }

    public String getGenero() {
        return genero;
    }

    public void setGenero(String genero) {
        this.genero = genero;
    }
    
    
}
